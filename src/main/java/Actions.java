public class Actions {
    public static int doSummation(int a, int b, int c){
        return a+b+c;
    }
    public int doMultiplication(int a, int b, int c){
        int result = a*b*c;
        return result;
    }
    public static int div(int a, int b) {
        return a / b;
    }
    public int sub(int a, int b, int c){
        int result = a-b-c;
        return result;
    }
}
